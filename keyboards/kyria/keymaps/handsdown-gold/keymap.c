#ifdef ENCODER_ENABLE
bool encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        // Volume control
        if (clockwise) {
            tap_code(KC_VOLD);
        } else {
            tap_code(KC_VOLU);
        }
    }
    else if (index == 1) {
        // Page up/Page down
            if (clockwise) {
                if (keyboard_report->mods & MOD_BIT (KC_LSFT)) {
                    clear_mods();
                    tap_code(KC_UP);
                    register_code(KC_LSFT);
                }
                else if (keyboard_report->mods & MOD_BIT (KC_LCTL)) {
                    clear_mods();
                    tap_code16(C(KC_U));
                    register_code(KC_LCTL);
                }
                else if (keyboard_report->mods & MOD_BIT (KC_LALT)) {
                    clear_mods();
                    tap_code(KC_RIGHT);
                    register_code(KC_LALT);
                }
                else {
                    tap_code(KC_PGUP);
                }
            }
            else {
                if (keyboard_report->mods & MOD_BIT (KC_LSFT)) {
                    clear_mods();
                    tap_code(KC_DOWN);
                    register_code(KC_LSFT);
                }
                else if (keyboard_report->mods & MOD_BIT (KC_LCTL)) {
                    clear_mods();
                    tap_code16(C(KC_D));
                    register_code(KC_LCTL);
                }
                else if (keyboard_report->mods & MOD_BIT (KC_LALT)) {
                    clear_mods();
                    tap_code(KC_LEFT);
                    register_code(KC_LALT);
                }
                else {
                    tap_code(KC_PGDN);
                }
            }
        }
    return true;
}
#endif


#ifdef HAPTIC_ENABLE
void play_haptic_pattern(uint16_t waveform) {
    haptic_enable();
    DRV_pulse(waveform);
    haptic_disable();
}

layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state)) {
        case _QWERTY:
            #ifdef TAPPING_TERM
                #undef TAPPING_TERM
                #define TAPPING_TERM 170
            #endif
            break;
        case _HANDSDOWN:
            #ifdef TAPPING_TERM
                #undef TAPPING_TERM
                #define TAPPING_TERM 275
            #endif
            break;
        case _COMMANDS:
            play_haptic_pattern(transition_hum);
            break;
        case _NUMPAD:
            play_haptic_pattern(pulsing_strong);
            break;
        case _SYMBOLS:
            play_haptic_pattern(transition_click);
            break;
        case _MEDIA:
            play_haptic_pattern(pulsing_sharp);
            break;
        case _GAMES:
            play_haptic_pattern(transition_rampup_med_smooth2);
            break;
        default:
            break;
    }
    return state;
}
#endif
